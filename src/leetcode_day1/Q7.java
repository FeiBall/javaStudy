public class Q7 {
    public int reverse(int x) {
        int ans = 0;
        while (x != 0) {
            int pop = x % 10;
            if (ans > Integer.MAX_VALUE / 10 || (ans == Integer.MAX_VALUE / 10 && pop > 7))
                return 0;
            if (ans < Integer.MIN_VALUE / 10 || (ans == Integer.MIN_VALUE / 10 && pop < -8))
                return 0;
            ans = ans * 10 + pop;
            x /= 10;
        }

        //x=527 超过511就溢出为例
        //pop=7 ans=0<51 则ans=7 x=52
        //pop=2 ans=5<51 则ans=72 x=5
        //pop=5 ans=72>51 判定溢出
        return ans;
    }
}
